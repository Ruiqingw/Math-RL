import os, sys, json, re, random
from collections import Counter, defaultdict

os.environ["HF_ENDPOINT"] = "https://hf-mirror.com"
os.environ["HF_DATASETS_CACHE"] = "/root/autodl-tmp/prm_grpo/datasets"

print("="*60)
print("Phase 0: Dataset Download & Exploration")
print("="*60)

from datasets import load_dataset

OUTPUT_FILE = "/root/autodl-tmp/prm_grpo/outputs/data_exploration_report.md"
os.makedirs(os.path.dirname(OUTPUT_FILE), exist_ok=True)

lines = []
def log(s=""):
    print(s)
    lines.append(str(s))

def save_report():
    with open(OUTPUT_FILE, "w") as f:
        f.write("\n".join(lines))
    print(f"\n[Saved] {OUTPUT_FILE}")

log("# Data Exploration Report")
log(f"Generated by explore_datasets.py\n")

# ─── Part 1: MATH-lighteval ───
log("## 1. MATH-lighteval (Policy Training Data)")
log("Downloading DigitalLearningGmbH/MATH-lighteval ...")

try:
    math_ds = load_dataset(
        "DigitalLearningGmbH/MATH-lighteval",
        cache_dir="/root/autodl-tmp/prm_grpo/datasets/math_lighteval"
    )
    log(f"\n### 1.1 Dataset Overview")
    log(f"- Splits: {list(math_ds.keys())}")
    for split in math_ds:
        log(f"- {split}: {len(math_ds[split])} examples")

    sample_split = list(math_ds.keys())[0]
    cols = math_ds[sample_split].column_names
    log(f"\n### 1.2 Columns")
    log(f"- {cols}")

    log(f"\n### 1.3 Sample Entry (first example from '{sample_split}')")
    ex = math_ds[sample_split][0]
    for k, v in ex.items():
        v_str = str(v)[:400] + ("..." if len(str(v)) > 400 else "")
        log(f"\n**{k}**:")
        log(f"```\n{v_str}\n```")

    train_split = "train" if "train" in math_ds else sample_split
    solutions = [math_ds[train_split][i]["solution"] for i in range(min(200, len(math_ds[train_split])))]

    has_boxed = sum(1 for s in solutions if r"\boxed{" in s)
    log(f"\n### 1.4 Solution Format Analysis (n=200)")
    log(f"- Solutions with \\boxed{{}}: {has_boxed}/200 ({has_boxed/2:.1f}%)")

    step_patterns = [
        (r"Step \d+", "Step N:"),
        (r"\n\n", "double newline paragraph"),
        (r"First,|Second,|Third,|Finally,", "ordinal words"),
        (r"\\begin\{align", "align env"),
    ]
    for pat, name in step_patterns:
        count = sum(1 for s in solutions if re.search(pat, s))
        log(f"- Has '{name}': {count}/200 ({count/2:.1f}%)")

    lengths = [len(s) for s in solutions]
    log(f"\n### 1.5 Solution Length Stats (chars, n=200)")
    log(f"- min={min(lengths)}, max={max(lengths)}, mean={sum(lengths)//len(lengths)}")

    if "type" in cols:
        types = Counter(math_ds[train_split]["type"])
        log(f"\n### 1.6 Type Distribution (train)")
        for t, c in types.most_common():
            log(f"- {t}: {c}")

    if "level" in cols:
        levels = Counter(math_ds[train_split]["level"])
        log(f"\n### 1.7 Level Distribution (train)")
        for l, c in sorted(levels.items()):
            log(f"- {l}: {c}")

    log(f"\n### 1.8 Three Random Examples")
    idxs = random.sample(range(len(math_ds[train_split])), 3)
    for i, idx in enumerate(idxs):
        ex2 = math_ds[train_split][idx]
        log(f"\n**Example {i+1}** (idx={idx}, type={ex2.get('type','?')}, level={ex2.get('level','?')})")
        log(f"*Problem*: {ex2['problem'][:300]}")
        log(f"*Solution (first 500 chars)*:\n```\n{ex2['solution'][:500]}\n```")

    log(f"\n### 1.9 IMPORTANT: Are solutions already step-formatted?")
    log("Checking for natural step boundaries...")
    sample5 = [math_ds[train_split][i]["solution"] for i in range(5)]
    for i, sol in enumerate(sample5):
        log(f"\n--- Solution {i+1} (full) ---")
        log(sol[:800])

    log(f"\n✅ MATH-lighteval complete.\n")
    save_report()

except Exception as e:
    import traceback
    log(f"\n❌ MATH-lighteval error: {e}")
    log(traceback.format_exc())
    save_report()

# ─── Part 2: PRM800K ───
log("\n## 2. PRM800K (Verifier Training Data)")
log("Downloading trl-lib/prm800k ...")

try:
    prm_ds = load_dataset(
        "trl-lib/prm800k",
        cache_dir="/root/autodl-tmp/prm_grpo/datasets/prm800k"
    )
    log(f"\n### 2.1 Overview")
    log(f"- Splits: {list(prm_ds.keys())}")
    for split in prm_ds:
        log(f"- {split}: {len(prm_ds[split])} examples")

    sample_split_prm = list(prm_ds.keys())[0]
    cols_prm = prm_ds[sample_split_prm].column_names
    log(f"\n### 2.2 Columns")
    log(f"- {cols_prm}")

    log(f"\n### 2.3 First Entry (full raw structure)")
    ex_prm = prm_ds[sample_split_prm][0]
    for k, v in ex_prm.items():
        v_str = str(v)[:800] + ("..." if len(str(v)) > 800 else "")
        log(f"\n**{k}** (type={type(v).__name__}):")
        log(f"```\n{v_str}\n```")

    train_prm = "train" if "train" in prm_ds else sample_split_prm

    log(f"\n### 2.4 Label Distribution (n=500)")
    all_labels = []
    step_counts = []
    for i in range(min(500, len(prm_ds[train_prm]))):
        row = prm_ds[train_prm][i]
        for lf in ["label", "labels", "completions", "steps", "rating", "ratings"]:
            if lf in row:
                val = row[lf]
                if isinstance(val, list):
                    all_labels.extend(val)
                    step_counts.append(len(val))
                else:
                    all_labels.append(val)
                break
    if all_labels:
        log(f"- Label distribution: {dict(Counter(all_labels))}")
        if step_counts:
            log(f"- Steps per example: min={min(step_counts)}, max={max(step_counts)}, mean={sum(step_counts)/len(step_counts):.1f}")

    log(f"\n### 2.5 Three Full Examples")
    idxs_prm = random.sample(range(min(1000, len(prm_ds[train_prm]))), 3)
    for i, idx in enumerate(idxs_prm):
        row = prm_ds[train_prm][idx]
        log(f"\n**PRM Example {i+1}** (idx={idx})")
        for k, v in row.items():
            log(f"  [{k}]: {str(v)[:800]}")

    log(f"\n✅ PRM800K complete.\n")
    save_report()

except Exception as e:
    import traceback
    log(f"\n❌ PRM800K error: {e}")
    log(traceback.format_exc())
    save_report()

log("\n## 3. Summary")
log("All datasets explored. See sections above for structure details.")
log("Report saved to: " + OUTPUT_FILE)
save_report()

print("\n" + "="*60)
print("ALL DONE. Report at:", OUTPUT_FILE)
print("="*60)
